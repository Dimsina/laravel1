     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update($id)
    {
        DB::table('users')
            ->where('id', $id)
            ->update(['name' => request('name'),
                'email' => request('email'),
                'password' => Hash::make(request('password')),
                ]);
        return redirect()->back();
    }

